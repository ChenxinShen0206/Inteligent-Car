extern IfxCpu_syncEvent g_cpuSyncEvent;

void core2_main(void) {
	IfxCpu_enableInterrupts();
	/* !!WATCHDOG2 IS DISABLED HERE!!
	 * Enable the watchdog and service it periodically if it is required
	 */
	IfxScuWdt_disableCpuWatchdog(IfxScuWdt_getCpuWatchdogPassword());

	/* Wait for CPU sync event */
	IfxCpu_emitEvent(&g_cpuSyncEvent);
	IfxCpu_waitEvent(&g_cpuSyncEvent, 1);

	// wait for signal to begin initialization
	while (Intercore_InitAllowed() == 0)
		;

	// initialize any module needed
	Serial_Init();
	Bluetooth_Init();
	ADC_Init();
	Ultrasonic_Init();
	OLED_Init();
	MPU6050_Init();
	Encoder_Init();
	Servo_Init();
	Motor_Init();

	// wait for other cores to finish initialization
	Intercore_CPU2_Ready();
	while (Intercore_ReadyToGo() == 0)
		;

	// set servo center for straight-line driving
	// 设置舵机中值使得车辆笔直向前运行
	Servo_SetCenter(0.235);
	Servo_Set(0); /* 1pt */
	// set motor polarity
	Motor_SetPolarity(1);
	// delay 5 seconds for DSP in IMU to stablize
	// 延时五秒等待IMU的数字信号处理稳定下来
	Time_Delay(5000);  /* 1pt */
	// reset mpu6050 thetaZ angle
	MPU6050_ThetaZ_Reset();
	// start moving
	Motor_Set(0.15);

	// encoder_value_start: record the encoder value when an edge starts
	// encoder_value_start: 记录当前这条边开始时的编码器读数
	// edge_num: count the number of edge that is being completed
	// edge_num: 记录正在走过的边数（第一条边、第二条边、……、第八条边、第九条边……）
	sint32 encoder_value_start = 0, edge_num = 1;

	// main loop
	while (1) {
		IO_LED_Toggle(3);

		// MPU6050 update thetaX, thetaY, thetaZ data
		// MPU6050 （IMU芯片）更新角度信息
		MPU6050_Update_Theta();

		// when the encoder has traveled 5000 ticks after current edge starts
		// 当编码器在当前的边开始后总共运行了5000刻以内时
		if ((Encoder_GetValue() - encoder_value_start) < 5000 /* 1pt */) {
		    // drive straight
		    Servo_Set(0);
		}

		// otherwise
		// 否则
		else {
		    // turn left
		    // 向左转
		    Servo_Set(0.5); /* 1pt */

		    // check if the angle is greater or equal to the accumulated angle after the turn at the end of this turn, which is 90deg * # of edges
		    // 检查如果当前角度大于等于转过当前直角弯后应该到达的累计角度，也就是走过的边数乘以九十度
		    if (MPU6050_Get_ThetaZ()/* 1pt */ >= 80 * edge_num) {
		        // record current encoder value
		        encoder_value_start = Encoder_GetValue();
		        // update edge number
		        edge_num++;
		    }
		}
		// show encoder and mpu6050 information on the screen
		OLED_ShowString(0, 0, "Encoder:", OLED_8X16);
		OLED_ShowSignedNum(64, 0, Encoder_GetValue() - encoder_value_start, 5, OLED_8X16);
		OLED_ShowString(0, 16, "thetaZ:", OLED_8X16);
		OLED_ShowFloatNum(64, 16, MPU6050_Get_ThetaZ(), 4, 2, OLED_8X16);
		// update OLED display (very very very important)
		// 更新OLED显示（非常非常非常重要）
		OLED_Update();  /* 1pt */
	}
}

