void Serial_Received(uint8 *dataptr, uint32 length, uint8 tag) {
    switch (tag) {
    case 1:
        // 假设tag=1表示接收控制指令
        if (dataptr[0] == 'S') {
            Motor_Set(0); // 停止电机
        }
        else if (dataptr[0] == 'F') {
            Motor_Set(0.15); // 前进
        }
        break;
    default:
        ;
    }
}

void Bluetooth_Received(uint8 *dataptr, uint32 length, uint8 tag) {
    switch (tag) {
    case 1:
        // 假设tag=1表示蓝牙遥控指令
        if (length > 0) {
            // 解析蓝牙数据并执行相应操作
            OLED_ShowString(0, 32, "BT:", OLED_8X16);
            OLED_ShowHexNum(32, 32, dataptr[0], 2, OLED_8X16);
            OLED_Update();
        }
        break;
    default:
        ;
    }
}

