void Periodic_100ms_ISR(void) {
	char buffer[20];
	float pot0, pot1, pot2;
	if (IO_DIP_Read(4)) {
		// read ADC value
		ADC_Read();
		pot0 = ADC_GetVoltage(0);
		pot1 = ADC_GetVoltage(1);
		pot2 = ADC_GetVoltage(2);
		// calculate coefficients
		if (pot0 > 0) {
			kp = kp_min + (kp_max - kp_min) * pot0 / 3.3;
		}
		if (pot1 > 0) {
			ki = ki_min + (ki_max - ki_min) * pot1 / 3.3;
		}
		if (pot2 > 0) {
			kd = kd_min + (kd_max - kd_min) * pot2 / 3.3;
		}
		// apply changes to PID handler
		PID_SetParams(&speed_pid, kp, ki, kd);
		// change displayed number
		sprintf(buffer, " %.3e ", kp);
		OLED_ShowString(30, 16, buffer, OLED_6X8);
		sprintf(buffer, " %.3e ", ki);
		OLED_ShowString(30, 28, buffer, OLED_6X8);
		sprintf(buffer, " %.3e ", kd);
		OLED_ShowString(30, 40, buffer, OLED_6X8);
		// keep sum cleared
		speed_pid.sum = 0;
	}
	sprintf(buffer, " %.3f  ", motor_output);
	OLED_ShowString(30, 52, buffer, OLED_6X8);
	// start new conversion for next round
	ADC_Start();
}

