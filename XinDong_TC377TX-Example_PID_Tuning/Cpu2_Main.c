extern IfxCpu_syncEvent g_cpuSyncEvent;

PID_Handler speed_pid;

double kp_min = 0, ki_min = 0, kd_min = 0;
double kp_max = 0.01, ki_max = 0.001, kd_max = 0.001;
double kp = 0, ki = 0, kd = 0;

sint32 last_encoder_value = 0;
float target_speed = 5000;	// unit:?

float motor_output = 0;

char intro[] = "PID Tuning Example:";
char kpstr[] = "Kp:", kistr[] = "Ki:", kdstr[] = "Kd:";

void core2_main(void) {
	IfxCpu_enableInterrupts();
	/* !!WATCHDOG2 IS DISABLED HERE!!
	 * Enable the watchdog and service it periodically if it is required
	 */
	IfxScuWdt_disableCpuWatchdog(IfxScuWdt_getCpuWatchdogPassword());

	/* Wait for CPU sync event */
	IfxCpu_emitEvent(&g_cpuSyncEvent);
	IfxCpu_waitEvent(&g_cpuSyncEvent, 1);

	// wait for signal to begin initialization
	while (Intercore_InitAllowed() == 0)
		;

	// initialize any module needed
	Serial_Init();
	Bluetooth_Init();
	ADC_Init();
	Ultrasonic_Init();
	OLED_Init();
	MPU6050_Init();
	Encoder_Init();
	Servo_Init();
	Motor_Init();
	PID_Init(&speed_pid);

	// wait for other cores to finish initialization
	Intercore_CPU2_Ready();
	while (Intercore_ReadyToGo() == 0)
		;

	// invert motor output
	Motor_SetPolarity(1);
    Servo_SetCenter(0.2);
	Servo_Set(0);


	// print fixed text
	OLED_ShowString(0, 0, intro, OLED_6X8);
	OLED_ShowString(12, 16, kpstr, OLED_6X8);
	OLED_ShowString(12, 28, kistr, OLED_6X8);
	OLED_ShowString(12, 40, kdstr, OLED_6X8);
	// apply change
	OLED_Update();
	Time_Delay(1000);

	// main loop
	while (1) {
		// some code to indicate that the core is not dead
		IO_LED_Toggle(3);
		Time_Delay_us(100000);
		OLED_Update();
	}
}

