void Periodic_PID_ISR(void) {
	sint32 temps32 = Encoder_GetValue();
	if (!IO_DIP_Read(4)) {
	    float currentSpeed = (temps32 - last_encoder_value) * 1000.0 / PID_PERIOD_MS;
	    char buffer[25];
	    sprintf(buffer, "%.3f\r\n", currentSpeed);
	    while(Serial_Transmit((uint8*)buffer, strlen(buffer)));

		motor_output = PID_Output(&speed_pid, target_speed, (temps32 - last_encoder_value) * 1000.0 / PID_PERIOD_MS);
		last_encoder_value = temps32;
		Motor_Set(motor_output / 1000);
	} else {
		Motor_Set(0);
	}
}

